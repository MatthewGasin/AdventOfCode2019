	public static boolean hasCrashed = false;
	
	public static void main(String[] args){
		long start = System.nanoTime();
		String[] empty = getEmptyTracks().split("\r\n");
		String[] input = getInput().split("\r\n");
		char[][] tracks = new char[input.length][input[0].length()];
		char[][] emptyTracks = new char[input.length][input[0].length()];

		ArrayList<Cart> carts = new ArrayList<Cart>();
		for(int i = 0; i < input.length; i++) {
			for(int j = 0; j < input[i].length(); j++) {
				emptyTracks[i][j] = empty[i].charAt(j);
				tracks[i][j] = input[i].charAt(j);
				if(isCart(tracks[i][j])) {
					carts.add(new Cart(j,i));
				}
			}
		}
		
				
		for(int tick = 0; tick < 100000; tick++) {
			//printState(tracks);
			//reset cart moves
			for(Cart c : carts) {
				c.nextTick();
			}
			for(int i = 0; i < tracks.length; i++) {
				for(int j = 0; j < tracks[i].length; j++) {
					//Find the first crash
					if(hasCrashed) {
						//break;
					}
					//look for carts!
					if(isCart(tracks[i][j])){
						Cart cur = null;
						for(Cart c : carts) {
							if(c.getX() == j && c.getY() == i) {
								cur = c;
								break;
							}
						}
						
						//if the cart has moved, don't move it again!
						if(cur.HasMoved()) {
							continue;
						}
						
						char above, below, left, right;
						if(j+1 < tracks[0].length) {
							right = tracks[i][j+1];
						}else {
							right = ' ';
						}
						if(j-1 > -1) {
							left = tracks[i][j-1];
						}else {
							left = ' ';
						}
						if(i+1 < tracks.length) {
							below = tracks[i+1][j];
						}else {
							below = ' ';
						}
						if(i-1 > -1) {
							above = tracks[i-1][j];
						}else {
							above = ' ';
						}
						
						//move the cart!
						//determine direction
						char nextMove = 'e';
						int nX = i;
						int nY = j;
						switch(tracks[i][j]) {
							case '<':
								nextMove = left;
								nX = j-1;
								nY = i;
								break;
							case '>':
								nextMove = right;
								nX = j+1;
								nY = i;
								break;
							case 'v':
								nextMove = below;
								nX = j;
								nY = i+1;
								break;
							case '^':
								nextMove = above;
								nX = j;
								nY = i-1;
								break;
							default:
								System.out.println("error in cart dir");
								break;
						}
						//direction is determined!
						//did we crash
						if(isCart(nextMove)) {
							System.out.println("Crash at pos " + nX + "," + nY);
							hasCrashed = true;
							Cart crashed = null;
							for(Cart c : carts) {
								if(c.getX() == nX && c.getY() == nY) {
									crashed = c;
									break;
								}
							}
							carts.remove(cur);
							carts.remove(crashed);
							tracks[i][j] = emptyTracks[i][j];
							tracks[nY][nX] = emptyTracks[nY][nX];
							if(carts.size() == 1) {
								System.out.println("The final cart is positioned at " + carts.get(0).x +"," + carts.get(0).y);
							}
							continue;
						}
						//move along!
						cur.move(nX, nY);
						
						switch(nextMove) {
							//move horizontal
							case '-':
								//move horizontal
								tracks[nY][nX] = tracks[i][j];
								break;
							case '|':
								//move vertical
								tracks[nY][nX] = tracks[i][j];
								break;
							case '+':
								//intersection!
								int t = cur.turn();
								if(t == 0) {
								//turn left
									if(tracks[i][j] == '>') {
										tracks[nY][nX] = '^';
									}else if(tracks[i][j] == '<') {
										tracks[nY][nX] = 'v';
									}else if(tracks[i][j] == '^') {
										tracks[nY][nX] = '<';
									}else if(tracks[i][j] == 'v'){
										tracks[nY][nX] = '>';
									}
								}else if(t == 1) {
								//go straight
									tracks[nY][nX] = tracks[i][j];
								}else {
								//turn right
									if(tracks[i][j] == '>') {
										tracks[nY][nX] = 'v';
									}else if(tracks[i][j] == '<') {
										tracks[nY][nX] = '^';
									}else if(tracks[i][j] == '^') {
										tracks[nY][nX] = '>';
									}else if(tracks[i][j] == 'v'){
										tracks[nY][nX] = '<';
									}
								}
								break;
							case '/':
								//turn accordingly
								if(tracks[i][j] == 'v') {
									tracks[nY][nX] = '<';
								}else if(tracks[i][j] == '>') {
									tracks[nY][nX] = '^';
								}else if(tracks[i][j] == '<') {
									tracks[nY][nX] = 'v';
								}else if(tracks[i][j] == '^') {
									tracks[nY][nX] = '>';
								}
								break;
							case '\\':
								if(tracks[i][j] == 'v') {
									tracks[nY][nX] = '>';
								}else if(tracks[i][j] == '<') {
									tracks[nY][nX] = '^';
								}else if(tracks[i][j] == '>') {
									tracks[nY][nX] = 'v';
								}else if(tracks[i][j] == '^') {
									tracks[nY][nX] = '<';
								}
								//turn accordingly
								break;
						}
					
					//Change the current spot to what it should be
					tracks[i][j] = emptyTracks[i][j];
					}

				}
			}
		}


		System.out.println("s: " + (System.nanoTime()-start)*Math.pow(10, -9));
	}

